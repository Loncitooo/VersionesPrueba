# Este es un ejemplo de archivo Python que realiza impresiones

# Imprime un mensaje de saludo
print("¡Hola, mundo!")

# Imprime un mensaje con variables
nombre = "Daniel"
edad = 21
print(f"Me llamo {nombre} y tengo {edad} años.")

# Imprime una lista de números
numeros = [1, 2, 3, 4, 5]
print("Números:", numeros)

# Imprime un bucle for
print("Contando hasta 5:")
for i in range(1, 6):
    print(i)

# Imprime una operación matemática
resultado = 10 + 20
print("Resultado de 10 + 20:", resultado)
